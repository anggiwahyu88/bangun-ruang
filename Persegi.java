public class Persegi {
    
    public static int Luas(int sisi) {
        int luas = sisi * sisi;
        return luas;
    }
    public static int Keliling (int sisi) {
        int keliling = sisi * 4;
        return keliling;
    }
    public static int Panjang_ruas ( int luas) {
        int panjang_ruas = luas / 4 ;
        return panjang_ruas;

    }
}


